// // Задача 50. Напишите программу, которая на вход принимает позиции элемента в двумерном массиве, 
// // и возвращает значение этого элемента или же указание, что такого элемента нет.
// // Например, задан массив:
// // 1 4 7 2
// // 5 9 2 3
// // 8 4 2 4
// // 17 -> такого числа в массиве нет


 void Main()
{
        int m = 3; // число строк
        int n = 4; // число столбцов

        // создаем пустой массив заданного размера
        int[,] array = new int[m, n];

        // заполняем массив случайными вещественными числами
        Random rand = new Random();
        for (int i = 0; i < m; i++)
        {
            for (int j = 0; j < n; j++)
            {
                array[i, j] = rand.Next(1, 10);
            }
        }

        // выводим массив на экран
        Console.WriteLine("Массив:");
        for (int i = 0; i < m; i++)
        {
            for (int j = 0; j < n; j++)
            {
                Console.Write(array[i, j] + " ");
            }
            Console.WriteLine();
        }

        // получаем позиции элемента от пользователя
        Console.Write("Введите номер строки: ");
        int row = int.Parse(Console.ReadLine());
        Console.Write("Введите номер столбца: ");
        int col = int.Parse(Console.ReadLine());

        // проверяем, что позиции элемента находятся в пределах массива
        if (row >= 0 && row < m && col >= 0 && col < n)
        {
            // выводим значение элемента
            Console.WriteLine("Значение элемента: " + array[row, col]);
        }
        else
        {
            // выводим сообщение об ошибке
            Console.WriteLine("Такого элемента нет.");
        }
}

Main();



